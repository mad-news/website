                    <div class="col-12 col-md-8">
                        <div class="single-post-text">
                            <div class="col-12 col-md-8">
                                <div class="single-post-text">
                                    <p><b>(一)什么是mRNA疫苗</b></p>
                                </div>
                            </div>
                            <p>mRNA疫苗是一类通过向人体细胞导入mRNA后产生抗原，从而达到免疫效果的新型疫苗。与依靠抗原或减毒病毒刺激免疫系统产生免疫反应的传统疫苗不同，RNA疫苗本身并不含有抗原，而是以编码抗原的mRNA为主要成分。这些编码抗原的mRNA能在细胞内被翻译为抗原蛋白。RNA疫苗中的mRNA通常由固体脂质纳米粒等特殊载体包裹，以保护脆弱的RNA分自并帮助RNA进入细胞内。通常来说，RNA疫苗发挥作用的步骤为：被载体包裹的RNA被注射到人体内、RNA进入细胞、RNA翻译为抗原蛋白、抗原蛋白被呈递至细胞表面或被分泌到细胞外引发免疫反应。</p>
                            <p>mRNA疫苗具有成本低，生产效率高的优点。因为生产周期短，不容易出现微生物污染问题。此外，因为mRNA疫苗的生产不需要使用有毒化学品，也可以做到无细胞生产，使mRNA疫苗理论上具有相较传统疫苗更高的安全性。另一方面，mRNA疫苗一般需要低温储存，而且储存时间较短，使mRNA疫苗的临床应用受到一定限制。</p>

