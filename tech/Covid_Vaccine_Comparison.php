                                <div class="single-post-text">
                                    <p><b>（二）什么是灭活疫苗</b></p>
                                </div>
                            </div>
                            <p>灭活疫苗是指由已杀灭的病原体制成的疫苗。制造灭活疫苗的过程中，杀灭病原体的方式包括：加热杀灭、利用化学方法（如甲醛）杀灭、以及利用放射线杀灭。一部分灭活疫苗中杀灭的病原体依然以完整形态存在，也有的灭活疫苗会将病原体裂解，亦存在将病原体中的抗原单独提纯出来的灭活疫苗。灭活疫苗主要通其中的抗原诱导细胞免疫的产生，一般来说需要多次接种且免疫效果持续较短。尽管灭活疫苗的工艺成熟高，但是由于生产过程需要进行细胞培养和病毒培养、建立毒株以及作为基质的细胞株的多级种子库，因此在生产阶段实现产能放大存在一定难度。灭活疫苗制作过程中需要保持免疫颗粒的完整性以保持疫苗的免疫效果，对灭活的工艺要求较高，且生产过程中要求的生物安全等级较高。另外，灭活工艺可能存在潜在的抗体依赖增强效应（ADE），主要表现为机体接种疫苗后，再次感染病毒时，对该病原的易感性增加或者疾病发病加重的现象。所以，灭活的研发需要警惕ADE现象的发生。</p>

