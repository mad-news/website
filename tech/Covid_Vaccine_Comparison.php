                            <p>目前，健康人接种国药疫苗是安全的，它可以有效降低感染COVID-19的概率。目前，国药疫苗尚不清楚接种疫苗后再次感染同种冠状病毒时，该病毒所致疾病是否会出现加重现象，这一点还需要在后续的临床试验中进一步观察。</p>
                            <div class="col-12 col-md-8">
                                <div class="single-post-text">
                                    <p><b>（六）综述</b></p>
                                </div>
                            </div>
                            <p>根据对这三种疫苗的有效率，安全性以及不良反应出现概率的比较，我们不难发现，辉瑞疫苗和Moderna疫苗的有效率要高于国药疫苗（但是辉瑞疫苗对于亚洲人群体的有效率实际上和国药疫苗差不多），然而辉瑞疫苗和Moderna疫苗出现不良反应的概率也要远远大于国药疫苗。这可能是因为辉瑞疫苗和Moderna疫苗采用的是新技术，所以临床应用尚不成熟而导致的结果。同时，辉瑞疫苗和Moderna疫苗的存储条件要比国药疫苗苛刻的多。但是另一方面，辉瑞疫苗和Moderna疫苗的制作要比国药疫苗方便，也便于量产。</p>
                            <p>最后，不论是辉瑞，Moderna, 还是国药，上市的时间都还太短，许多重要的信息，比如说疫苗保护的持续时间，都还不能明确，因此，个人不建议在非紧急的情况下急着接种疫苗。在北美的朋友，如果要打疫苗，最好选择Moderna疫苗，它对于亚洲人的有效率要比辉瑞疫苗高。但是，Moderna疫苗出现不良反应的概率要比辉瑞疫苗要大，好在这些不良反应大多都是轻微的，因此可以放宽心接种。对于最近有回国计划的朋友，不建议在美国接种疫苗。因为接种疫苗后，你的体内就会生成抗体，这样是无法通过“血清抗体双阴性”检测的。在国家还没有明确哪种疫苗可以接种的情况下，最好还是不要打疫苗。国内的朋友可以放心接种国药疫苗，它的不良反应概率要低得多，而且79%左右的有效率对于疫情不是特别严重的中国而言已经足够了。是否打疫苗是个人的选择，麦迪讯在此也只是给出一个建议，希望大家能慎重选择。祝愿大家在2021年能身体健康，度过这段疫情肆虐的艰难时光。</p>

