                            <div class="col-12 col-md-8">
                                <div class="single-post-text">
                                    <p><b>（三）辉瑞疫苗分析</b></p>
                                </div>
                            </div>
                            <p>辉瑞疫苗是由辉瑞制药有限公司和德国生物科技公司BioNTech联合研发的一款疫苗。注射方式为两次注射，间隔21天，一般使用上臂肌肉注射的方法。根据CDC的官网信息，建议16岁及以上的人接种辉瑞疫苗。如果你对mRNA COVID-19 疫苗中的任何成分存在严重过敏反应（过敏性反应）或速发性过敏反应（即使不严重），你都不应该接种mRNA COVID-19 疫苗。如果你在接种第一剂疫苗后出现严重过敏反应（过敏性反应）或者速发型过敏反应（即使不严重），你都不应该接种第二剂mRNA COIVD-19 疫苗。速发性过敏反应是指在接种疫苗4小时内出现反应，症状包括荨麻疹，肿胀或气喘（呼吸窘迫）。对于聚乙二醇（PEG）或聚山梨醇酯过敏的人不应接种mRNA COVID-19 疫苗。</p>
                            <p>辉瑞疫苗常见的副作用有：接种部位疼痛，接种部位肿胀，接种部位发红，怕冷，疲劳和头疼。这些·副作用通常在接种疫苗后一两天内开始出现，可能感觉像流感症状，甚至可能影响你进行日常活动的能力，但几天后就会消失。在参与实验的38000名志愿者中，根据疫苗和安慰剂一比一比例的双盲测试结果显示，大概有84.1%的人出现了接种部位的反应，62.9%的人出现了疲劳的症状，55.1%的人出现了头痛的症状，38.3%的人出现了肌肉疼痛的症状，23.6%的人出现了关节痛的症状，14.2%的人出现了发烧的症状，严重不良反应的人数在0%到4.6%之间，且第二剂出现的概率要比第一剂大。55岁以上老人出现不良反应的频率要高于年轻人。</p>
                            <p>辉瑞疫苗含有核酸编辑过的mRNA，用来编译SARS-COV-2中的刺突糖蛋白，从而使免疫系统识别并产生相对应的抗体。疫苗需要保存在零下60度到零下80度的低温环境中。</p>
                            <p>根据辉瑞疫苗的三期数据，在主要疗程内（即接种第二剂后七天里）对于之前没有感染COVID-19的参与者，疫苗的有效率高达95%。综合了之前有感染和没感染的数据，疫苗的有效率也高达94.6%。值得一提的是，辉瑞疫苗对于亚洲人的有效性相对较小，只有74.4%。辉瑞疫苗对于男性的有效率较高，为95.3%，女性相对小一点，为93.9%。更多的数据详见下表。注射了第一剂疫苗后，有效率预计在82%，不过CDC没有测算如果超过21天没有注射第二剂时疫苗的有效率。</p>

