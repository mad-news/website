                        <div class="single-post-text">

                            <p>从2020年初开始，COVID-19疫情已经持续了整整一年的时光。在这一年的时间里，COVID-19的序列组，治疗方法和手段，以及疫苗的研发都在如火如荼地进行着。目前市面上已经有六种疫苗，分别是两种mRNA疫苗（辉瑞-BioNTech 的 Tozinameran 和 Moderna 的 mRNA-1273）， 两种常规灭活疫苗（国药集团的BBIBP-CorV 和科兴生物的克尔来福），以及两种病毒载体疫苗（加马列流行病与微生物学国家研究中心的Gam-COVID-Vac（俄罗斯卫星V号疫苗），牛津大学和阿斯利康制药的AZD1222）。其中，只有三种疫苗获得了部分国家的全面授权，即辉瑞疫苗，Moderna 疫苗和国药集团的疫苗。那么，mRNA疫苗和常规灭活疫苗有什么区别？这三种疫苗的安全性如何？会有什么副作用？有效率又如何？有怎么样的接种方式？本文将会针对这些问题全面分析和比较这三种疫苗目前的公开数据，希望能给读者们一点启迪，让读者们明白各种疫苗的优劣，从而做出稳妥的选择。</p>
