                            <p>目前，接种Moderna疫苗的好处有：减少接种第二剂后至少十四天内感染COVID-19的可能性，减少接种第二剂后至少十四天内患严重COVID-19疾病的可能性。目前仍然没有定论的是疫苗保护的持续时间，在某些可能患严重COVID-19疾病的高风险人群内疫苗的有效性，之前得过SARS-COV-2人群中疫苗的有效性，疫苗在小儿群体中的有效性，对于未来可能的COVID-19变种疫苗的有效性，疫苗对于无症状患者的有效性，疫苗对于COVID-19死亡率的影响，疫苗对于阻断SARS-COV-2传播的有效性。目前接种Moderna疫苗未知的风险有：在某些人群中的安全性，某些可能存在的罕见不良反应，以及疫苗引发的疾病。</p>
                            <div class="col-12 col-md-8">
                                <div class="single-post-text">
                                    <p><b>（五）国药疫苗分析</b></p>
                                </div>
                            </div>
                            <p>国药疫苗是由中国生物北京公司开发的一款灭活疫苗，其原理就是把死去的病毒的衣壳打入人体内，使人体产生免疫应答反应，从而产生相对应的抗体。由于目前网上暂时还没有国药疫苗三期数据的论文，所以这里就引用一二期的论文以及北京生物公司的公告，来大致了解一下这款疫苗的特性。</p>
                            <p>根据国药疫苗的公告，三期疫苗的有效率达79.34%。目前建议年龄在18岁到60岁的人群接种。对于既往发生过疫苗接种严重过敏反应；患有血小板减少症或出血性疾病者；患有惊厥、癫痫、脑病、其他进行性神经系统疾病和精神疾病史或家族史；严重的肝肾疾病、药物不可控制的高血压、糖尿病并发症、恶性肿瘤；已被诊断为患有先天性或获得性免疫缺陷、HIV感染、淋巴瘤、白血病或其他自身免疫疾病、已知或怀疑患有严重呼吸系统疾病、严重心血管疾病、恶性肿瘤；以上人群皆不建议接种。</p>
                            <p>国药疫苗需要接种两剂，两剂间隔14-28天，接种方式是上臂肌肉注射。已知的不良反应有接种部位疼痛，局部瘙痒，肿胀，硬结和红晕等。全身不良反应以疲劳乏力为主，还包括发热、肌肉痛、头痛、咳嗽、腹泻、恶心、厌食和过敏等。根据国药疫苗的一二期数据来看，29%的志愿者在疫苗和安慰剂比例为三比一的双盲实验中出现了不良反应，最常见的不良反应为疼痛，有16%的志愿者出现了这一不良反应。其次，则是疲劳乏力，有3%的志愿者出现了这一反应。然后，是发烧和肿胀，都有2%的志愿者出现了这一不良反应。其余的不良反应出现的比例都小于或等于1%，再次就不赘述，详情请看下表。</p>

