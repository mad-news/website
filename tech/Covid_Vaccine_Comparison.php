                            <p>目前，接种辉瑞疫苗的好处有：减少接种第二剂后七天内感染COVID-19的可能性，减少接种了第一剂还没接种第二剂时感染COVID-19的可能性，减少接种了第一剂后患严重的COVID-19的可能性。目前仍然没有定论的是疫苗保护的持续时间，在某些可能得严重的COVID-19的高风险人群中的有效性，感染了SARS-COV-2后疫苗得有效性，疫苗在小儿群体中得有效性，未来遇到变种的病毒时疫苗的有效性，疫苗对于无症状患者的有效性，疫苗之于COVID-19的长期影响，疫苗对于COVID-19流行病死亡率的影响以及疫苗对于阻断SARS-COV-2传播的有效性。目前接种辉瑞疫苗，未知的风险可能有：在某些人群的副作用调查还不明确（比如年龄低于16岁的儿童），可能存在某些罕见的不良反应，以及疫苗引发的疾病。</p>
                            <div class="col-12 col-md-8">
                                <div class="single-post-text">
                                    <p><b>（四）Moderna 疫苗分析</b></p>
                                </div>
                            </div>
                            <p>Moderna 疫苗是由ModernaTX 公司开发的一款mRNA疫苗。注射方式同样为两次注射，相隔一个月（28天）接种。接种方法同样为上臂肌肉注射。根据CDC的官网信息，建议18岁及以上的人接种Moderna疫苗。如果你对mRNA COVID-19 疫苗中的任何成分存在严重过敏反应（过敏性反应）或速发性过敏反应（即使不严重），你都不应该接种mRNA COVID-19 疫苗。如果你在接种第一剂疫苗后出现严重过敏反应（过敏性反应）或者速发型过敏反应（即使不严重），你都不应该接种第二剂mRNA COIVD-19 疫苗。速发性过敏反应是指在接种疫苗4小时内出现反应，症状包括荨麻疹，肿胀或气喘（呼吸窘迫）。对于聚乙二醇（PEG）或聚山梨醇酯过敏的人不应接种mRNA COVID-19 疫苗。</p>
                            <p>Moderna 疫苗常见的副作用有：接种部位的疼痛，接种部位肿胀，接种部位发红，怕冷，疲劳和头疼。这些副作用通常在接种疫苗后一两天内开始出现，可能感觉更像流感症状，甚至可能影响你进行日常活动的能力，但几天后就会消失。在参与实验的27817名志愿者中，根据疫苗和安慰剂一比一比例的双盲测试的结果显示，大概有91.6%的人出现了接种部位的疼痛，68.5%的人出现了疲劳的症状，63%的人出现了头疼的症状，59.6%的人出现了肌肉酸痛的症状，44.8%的人出现了关节疼痛的症状，43.4%的人感到了身体寒冷。严重的不良反应的人数在0% 到9.7%之间，且第二剂出现的概率比第一剂大。65岁以上老人出现严重不良反应的概率要高于年轻人。</p>
                            <p>Moderna疫苗同样是通过编辑过的mRNA，来编译SARS-COV-2中的刺突糖蛋白，从而使免疫系统识别并生成相应的抗体。疫苗需要保存在零下15度到25度的环境中。</p>
                            <p>根据Moderna疫苗的三期数据，在主要疗程内（即接种第二剂后七天里），疫苗的有效率高达94.5%。值得一提的是，对于亚洲人来说，Moderna疫苗的有效性为100%。Moderna 疫苗对于男性的有效率为95.5%，女性相对小一点，为93.5%。更多数据详见下表。在注射第二剂后十四天，疫苗的有效率为94.1%。疫苗对于65岁以上老人的有效率（86.4%）低于年轻人的有效率（95.6%）。注射了第一剂疫苗后，有效率在80.2%，不过CDC没有测算超过28天没有注射第二剂时疫苗的有效率。</p>

