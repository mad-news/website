                    <div class="col-12 col-md-8">
                        <div class="single-post-text">
                            <p>
                                衡水中学张锡峰同学自“土猪拱白菜”引发的争议正在继续发酵。高考结束后，父母开汽车来接他同样引发了激烈的讨论。有网友认为，张锡峰同学的家庭并不拮据，也和很多家庭一样有房有车，这与张锡峰同学自己在演讲中所描述说的：看着其他同学被轿车接走而心中“翻江倒海”的场景是有出入的。不仅如此，张锡峰作为一个在衡水中学这个在河北省内“掐尖”与聚集优质教育资源的“超级学校”。同时，张同学的公共表达能力十分出众，很明显受过专门训练，甚至超过很多留洋学生的演讲水平。<p style="color: grey;">*（衡水中学高中的学费为9360元/生/学期，而根据河北公办普通高中学费标准，市区省级示范性高中每生每学期最高限价1000元）</p></p>
                            <p>
                                因此，张同学无论怎么看都与他自己在演讲所极力想要塑造的来自乡村的“土猪”形象大相径庭。有不少人都觉得，张同学的家庭虽并非大富大贵，但也应当是比较典型的城镇中产阶级家庭，而非张同学自己说的“来自底层的土猪”。</p>
                            <p>
                                在我们看来，张同学是一个出色的鼓动家，而且其私人生活没有什么值得大家指摘或干涉的。相反，我们这些历史和政治专业的编委，非常感谢张同学在演讲中抛出了一个非常重要的命题：在过去和当下，阶级究竟意味着什么？长久以来，我们要么沉浸在被染上革命浪漫主义油彩的“阶级斗争故事”中，一厢情愿地认为“阶级在社会主义祖国已经被消灭”；要么面对种种社会弊病，一味大谈“阶级固化”、“内卷”或“两极分化”，但又不知道“阶级”为何物。</p>
                            <p>
                                借着高考的喧嚣结束，国内的同学们放暑假之际，<b>让我们一起来来聊聊“阶级”这个概念究竟代表了什么？同时，针对张同学在演讲中流露出的，对于成为“一眼就能看到死”的“普通人”的不甘，我们也期待大家一起来思考：高考究竟能不能帮助普通人实现阶级跃升？</b></p>
                            <p>
                                在此，我想介绍我校UW-Madison前教授，美国著名的马克思主义社会学家埃里克·奥林·赖特在借鉴了马克思，韦伯与布赫迪厄三位大师关于阶级的构想后所搭建的一个阶级分析的框架。在这个框架中，他将对于阶级分析的方式分为了三个大类：</p>
                            <p></b><font color="#d23d67"><b>
                                <ol>
                                    <li>阶级作为剥削与支配关系(class as exploitation and domination)</li>
                                    <li>阶级作为个体属性(class as individual attributes)</li>
                                    <li>阶级作为机会阻隔(class as opportunity hoarding)</li>
                                </ol>
                            </b>
                            </font></b>
                            </p>
                            <div class="col-12">
                                <div class="single-post-thumb">
                                    <center>
                                        <img src="img/2_2.png" alt="2_2" width="750">
                                        <p style="color:grey; line-height: 50px">赖特教授遗像（马克思主义中文网）</p>
                                    </center>
                                </div>
                            </div>
                            <p>接下来，我将结合张同学的案例，简要地阐释赖特教授总结出的三种不同的阶级分析方式。并以此向已经仙逝的赖特教授致敬。在此，我想阐明我的观点：<b><font color="#d23d67">我认为，张锡峰同学的演讲中提到的以“白菜与土猪”为特征的“阶级”更多体现为一种机会的阻隔（class as opportunity hoarding）。</font></b>同样，他的言论所引发的争议同样也体现了人们对于阶级作为机会阻隔的巨大焦虑。为什么？且听我慢慢道来。</p>
                        </div>
                    </div>



                    <div class="col-12 col-md-8">
                        <div class="single-post-text">
                            <p><b><i>01</i></b></p>
                            <p><b># 阶级作为剥削与支配关系 | class as exploitation and domination</b></p>
                            <p>
                                这一基于马克思主义传统的阶级分析方式可能是许多中文读者最为熟悉，但又略有些陌生的。说它熟悉，凡是上过政治课的人或多或少都对于马克思的理论有所了解；而说它陌生，是因为我们并不知道如何实际运用这一阶级分析方式来看问题。 </p>
                            <p>
                                在当今的资本主义社会中，根据对于生产资料占有的情况不同分为了两个主要的阶级，<b><font color="#d23d67">即占有生产资料的资产阶级与不占有生产资料并出售自己劳动的无产阶级。</font></b>在这两个阶级之间很明显存在着剥削与主宰的关系。</p>
                            <p>
                                通过占有生产资料，资产阶级可以获得无产阶级劳动的剩余价值作为“利润”，这一关系便是所谓的<b><font color="#d23d67">剥削（exploitation）</font></b>。同时，资产阶级对于生产资料的占有更让他们可以控制与决定被其雇佣的无产阶级的活动，而这一关系便是<b><font color="#d23d67">支配（domination）</font></b>。</p>

                                <div class="col-12">
                                    <div class="single-post-thumb">
                                        <center>
                                            <img src="img/2_3.png" alt="2_3" width="750">
                                            <p style="color:grey; line-height: 50px"> （电影：摩登时代）</p>
                                        </center>
                                    </div>
                                </div>

                            <p>
                                简单来讲，一个工厂的拥有者可以在一定范围内控制雇工的活动，如几点上下班，着装要求，午休时间，进行何种劳动。不仅如此，老板们还从这些雇工的劳动中赚到了利润。这便是阶级作为剥削与支配关系的体现。</p>
                            <p>
                                基于马克思主义传统的阶级作为剥削与主宰关系是赖特的阶级分析框架中最为核心与重要的一环。这一阶级分析方式同时在社会关系的层面上联系了不同阶级的经济状态与经济活动。</p>
                            <p>
                                在宏观层面上，这一阶级分析方式犹如一根龙骨，贯穿了整个当代资本主义经济下的社会并体现出这样的社会中不可调和的最为根本的矛盾。</p>
                            <p>
                                但是在微观的层面上，这一阶级分析的方式还是需要做一定的补充。资产阶级与无产阶级并非当前资本主义社会中唯二存在的阶级，正如马克思与恩格斯在《共产党宣言》里所描述的仅少量占有生产资料却不去购买劳动的小资产阶级（也就是很多人常讲的“小布尔乔亚 petite bourgeosie 一词），如手工艺人，小店老板等。</p>
                            <p>
                                在19世纪，马克思与恩格斯所认为的这些小资产阶级会在现代工业资本主义下衰落并消失。然而在21世纪的今天，小资产阶级还是广泛存在于大多数社会中。同时，也有许多并不占有生产资料同时出售自己劳动的人，他们依靠着对特殊的技能和知识，也过着不同于一般无产阶级生活。如知识分子、薪水不低的高级技工，或城镇中产阶级。同时，现代金融业也一定程度上允许了非资产阶级碎片化地占有微小的生产资料。如此在微观上，不同的角度与阶级分析方式的补充可以更加完善我们对于“阶级”与社会关系的理解。</p>
                            <p>
                                正如在张锡峰同学的例子中，我们很难从他家庭在对于生产资料的占有情况分析出他所属的阶级，<b><font color="#d23d67">但却可以从他父母所开的轿车，就读的学校与谈吐姿态中推断他来自于一个城镇中产阶级家庭，</font></b>这便是我们接下来将要讨论的将阶级作为个体属性进行分析的方式。</p>
                        </div>
                    </div>

                    <div class="col-12 col-md-8">
                        <div class="single-post-text">
                            <p><b><i>02</i></b></p>
                            <p><b># 阶级作为个体属性 | class as individual attributes</b></p>

                            <p>一个人的性别，宗教信仰，年龄等都属于一个人的个体属性。在一个人的众多个体属性中，有相当一部分属性与阶级这一概念息息相关。这个人开着什么品牌的汽车，住着如何的住宅，收入多少，接受过何种教育，乃至于认识什么人（正如布赫迪厄所说的：社会资本social capital这一概念）等等。</p>
                            <p><b><font color="#d23d67">以上这些个人特征与一个人的物质条件的联系便是作为个体属性的阶级，这种阶级分析的方式也是我们生活中所最常用的。</font></b></p>
                            <p>通常地，我们把一个享有类似的个体属性的集群认作是一个阶级。譬如一个人如果坐着豪车与别墅，有着极庞大的收入与高质量的社会联系，我们便会认为这是一个属于上层阶级的人。</p>
                            <p>又比如，一个人的行为举止，神态气质被认为和所属阶级息息相关，而金钱有时也不能弥合这些举止和气质的差距。</p>
                            <p>举一个有趣的例子，在《显贵的主顾》一案中，面对暴发户加流氓格鲁纳，佛尔摩斯非常高冷地奚落他：</p>
                            <p><i>“好自为之吧，如果你指望被英国社交界所接受，你就要学会在点雪茄之前，先把外头的标签撕掉，否则人家会永远瞧不起你。”</i></p>


                    <div class="col-12">
                        <div class="single-post-thumb">
                            <center>
                                <img src="img/2_4.png" alt="2_4" width="720">
                                <p style="color:grey; line-height: 50px">(youtube)</p>
                            </center>
                        </div>
                    </div>

                            <p>而在现代中国年轻人中，教育愈来愈能体现出阶级概念的个体属性。出国留学总让人联想到上层中产阶级及高净值人群；有国内高等顶尖学府的学历的人更习惯被大家划入中产阶级的行列。另一方面，一个接受职业教育的人，或完全没接受高等教育的人更可能是偏向底层与边缘化阶级的一员。</p>
                            <p>正如我们在前文中所提到的，张锡峰同学的家里所开的汽车，他所就读的学校，乃至于他的言谈举止等微观层面上的个体属性成为了推断他所属阶级的具体线索。但是，一个人乃至于他/她所属的阶级是如何获得这些个体属性的呢？针对这个问题布赫迪厄于微观层面上基于个体属性的阶级分析无法做出详尽的解释，但基于马克思主义传统的阶级作为剥削与支配关系的分析，还有韦伯提出的阶级作为机会阻隔的分析可以为我们解释人与其所属阶级是如何取得这些个体属性。我将在下一段讲解社会学奠基人韦伯的阶级分析方式。</p>
                        </div>
                    </div>


                    <div class="col-12 col-md-8">
                        <div class="single-post-text">
                            <p><b><i>03</i></b></p>
                            <p><b># 阶级作为机会阻隔 | class as opportunity hoarding</b></p>
                            <p>在韦伯的社会阶层概念中，<b><font color="#d23d67">“权力的占有”（possession of power）与“权力的使用”（exercise of power）</font></b>的能力在阶级的形成上起到了极大的作用。而我们要谈的阶级作为机会阻隔这一分析方式便是基于不同阶级所具有的不同的“权力的占有”能力上的。<b><font color="#d23d67">“权力的占有”指的是一个人对于各类不同的但有限的“社会资源”的占有能力，这里的社会资源既可以实体的土地，机器，金钱或其他的资本，也可以是社会上的尊重，话语权，乃至于知识与教育。</font></b></p>
                            <p>这种对于社会资源的占有不仅是对自身阶级的巩固，也是把更低阶级的人排出在“社会资源”之外的能力，也就是所谓的机会阻隔。基于韦伯的概念（同时也包括一些马克思传统），资本主义社会大致分为了3个阶级：</p>
                            <dl>
                                <dd>- 最顶层的资产阶级对于绝大多数社会资源有着极强的占有能力，尤其是他们对于生产资料，也就是资本的占有让他们有着极大的特权；</li>
                                <dd>- 中产阶级在韦伯的概念中有了更为清晰的表现，虽然他们当中很大多数对于生产资料与资本的占有能力十分弱小与无产阶级别无二致，很多时候也需要向资产阶级出售劳动。但他们却在如专业知识，教育，亦或是发言权上面相较于无产阶级有着极大的优势，而这些优势（尤其是专业知识与教育上的）也带给了他们相较于无产阶级在经济上的优势；</li>
                                <dd>- 无产阶级在各种社会资源的占有上都极为劣势，他们许多因为在发言权与社会关注上弱小的占有而被社会边缘化。</li>
                            </dl>
                            <p>说到这儿我们大概可以看出，衡水中学这样的“超级中学”里的中产阶级学生是如何从社会底层处“卷走了”高中教育资源和高等教育资源，并用高昂的学费巩固了这些教育资源。<b><font color="#d23d67">同时，张锡峰同学的激情演讲，也生动地为我们展示了中产阶级对于话语权与社会关注的占有能力。话语权与机会阻隔产生合力，使得一个城镇中产阶级的孩子夺取了人们对社会底层的同情。</font></b></p>
                        </div>
                    </div>



                    <div class="col-12 col-md-8">
                        <div class="single-post-text">
                            <p><b><i>小结</i></b></p>

                    <div class="col-12">
                        <div class="single-post-thumb">
                            <center>
                                <img src="img/2_5.png" alt="2_5" width="300">
                                <p> </p>
                            </center>
                        </div>
                    </div>

                            <p>张锡峰同学的演讲为我们更全面地认识“阶级”这个概念提供了良好的契机。同时，正如马克思所说：“哲学家们只是用不同的方式解释世界，而问题在于改变世界。”光是认识阶级还是不够的，同样还要积极地探究改善“阶级固化”的方法。</p>
                            <p>张同学的演讲提供了一种解决阶级固化的现成方法，并迎合了社会上很多人的观念，也就是：“高考是给底层实现阶级跃迁的机会，而且这个机会较为公平”。所以他在演讲中将自己打扮成一个来自农村的“土猪”要通过高考去拱到城里的“白菜”，亦或是阶级跃迁的机会。</p>
                            <p>但通过阅读上文介绍的三种阶级分析方式，我相信读者们不难看出，<b><font color="#d23d67">正是他的演讲与他的真实家庭情况，反映出高考不能完全等同于阶级跃迁的机会。相反，在某些情况下，高考已经变成用机会阻隔维护现有阶级的机制，或者，我们用一个新词来说，是阶级再生产的工具。</font></b></p>
                            <p>那么，什么是教育的阶级再生产？简而言之：</p>

                    <div class="col-12">
                        <div class="single-post-thumb">
                            <center>
                                <img src="img/2_6.png" alt="2_6" width="750">
                                <p> </p>
                            </center>
                        </div>
                    </div>
                        <p>底层的孩子接受底层的教育，取得底层的分数，进入底层的学府，产生底层的观念，继续底层的地位和资源。</p>

                        <div class="col-12">
                            <div class="single-post-thumb">
                                <center>
                                    <img src="img/2_7.png" alt="2_7" width="750">
                                    <p> </p>
                                </center>
                            </div>
                        </div>

                        <p>上层的孩子接受上层的教育，取得底层的分数，进入上层的学府，产生上层的观念，继续上层的地位和资源，</p>
                        <p>如果按照这个模型，高考作为公平竞争的平台，是否真的遇到危机了呢？让我们在接下来的文章中，继续探究这个问题吧！</p>

